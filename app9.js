let a =5 ,b=10;

//เพิ่มค่าprofix
console.log("ค่าเริ่มต้น=",a);
console.log("เพิ่มค่าเเบบprefix+",++a);
console.log("ค่าปัจจุบัน=",a);


a=5;
//เพิ่มค่าเเบบ postfix
console.log("ค่าเริ่มต้น=",a);
console.log("เพิ่มค่าเเบบpostfix+",a++);
console.log("ค่าปัจจุบัน=",a);


//ลดค่าprofix
console.log("ค่าเริ่มต้น=",b);
